package com.dataguise.webservices.beans;

public class FtpAgentConfig {
	
	Integer ftpLetId;
	String hostName;
	String portNo;
	
	 
	public String getHostName() {
		return hostName;
	}
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}
	public String getPortNo() {
		return portNo;
	}
	public void setPortNo(String portNo) {
		this.portNo = portNo;
	}
	public Integer getFtpLetId() {
		return ftpLetId;
	}
	public void setFtpLetId(Integer ftpLetId) {
		this.ftpLetId = ftpLetId;
	}
	
	
	
	

}
